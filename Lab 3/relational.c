// relational operators without if else

#include <stdio.h>

int main() {
    int a = 5, b = 5, c = 10;

    // without if else

    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);
    printf("a > c: %d\n", a > c);
    printf("a < c: %d\n", a < c);
    printf("a >= b: %d\n", a >= b);
    printf("a <= c: %d\n", a <= c);


    return 0;
}